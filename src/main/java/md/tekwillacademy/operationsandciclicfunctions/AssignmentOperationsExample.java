package md.tekwillacademy.operationsandciclicfunctions;

public class AssignmentOperationsExample {
    public static void main(String[] args) {
        int a = 65;
        int b = 40;

        int result  = a;
        System.out.println(result);

        result +=b; // result = result +b;
        System.out.println("Sum is: " + result);

        result -= a;  // result = result -a;
        System.out.println("Substraction is: " + result);

        result *=5; // result *5 ; rssult = 40*5;
        System.out.println("Multiplication is: " + result);

        result /=10;  // result = result / b;
        System.out.println("Devision is: " + result);

        result %= a;  // result = result % b;
        System.out.println("The rest of devision: " + result);








    }


}
