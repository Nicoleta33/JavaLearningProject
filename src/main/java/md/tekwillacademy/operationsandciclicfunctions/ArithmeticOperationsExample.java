package md.tekwillacademy.operationsandciclicfunctions;

public class ArithmeticOperationsExample {
    public static void main(String[] args) {
        int a  = 48;
        int b = 10;

        int result =a+b;  // sum

        System.out.println("Adunarea = " + result);

        result =a-b;  // substraction

        System.out.println("Scaderea = " + result);

        result = a*b;  // multiplication
        System.out.println("Inmultirea = " + result);

        result = a/b;  // devision
        System.out.println("Impartirea = " + result);

        result  = a % b;  // the rest of devision with remainder
        System.out.println("Impartirea: " + result);



    }


}
